package ReqresTest.POJO;

public class SuccessReg {
    private String id;
    private String token;

    public SuccessReg(String id, String token) {
        this.id = id;
        this.token = token;
    }

    public String getId() {
        return id;
    }

    public String getToken() {
        return token;
    }
}
